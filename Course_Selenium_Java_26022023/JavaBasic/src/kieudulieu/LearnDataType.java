package kieudulieu;

public class LearnDataType {

    /*
        KIỂU DỮ LIỆU :
            - Dấu ngoặc kép đại diện cho 1 chuỗi . VD : "OK"
            - Dấu nháy đơn đại diện cho 1 kí tự . VD : 'OKK'
    */
    public static void main(String[] args) {
        //String
        String name = "Automation"; //=> Lưu chuỗi
        //int
        int age = 10; // Lưu số nguyên
        //byte
        byte numByte = -128; // Lưu từ  -128 đến 127
        //char
        char varChar = 'a'; //Lưu 1 kí tự
        //boolean
        boolean check1 = true;
        boolean check2 = false;
        //float
        float numFloat = 5.5f; // Lưu số thực
        //long
        long numLong = 4; // Lưu số nguyên


    }

}
